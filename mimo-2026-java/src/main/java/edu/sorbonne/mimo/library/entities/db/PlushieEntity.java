package edu.sorbonne.mimo.library.entities.db;

import edu.sorbonne.mimo.library.entities.Plushie;
import edu.sorbonne.mimo.library.entities.PlushieCategory;
import jakarta.persistence.*;

import java.util.Optional;

@Entity
@Table(name = "plushies")
public class PlushieEntity {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(name = "name", nullable = false)
    private String name;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "brand_id", nullable = false)
    private BrandEntity brand;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "distributor_id", nullable = false)
    private DistributorEntity distributor;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "factory_id", nullable = false)
    private FactoryEntity factory;

    @Column(name = "category")
    private String plushieCategory;

    public PlushieEntity() {}

    public PlushieEntity(String name, BrandEntity brand,
                      DistributorEntity distributor, FactoryEntity factory, String plushieCategory) {
        this.name = name;
        this.brand = brand;
        this.distributor = distributor;
        this.factory = factory;
        this.plushieCategory = plushieCategory;
    }

    // Getters and setters
    public Long getId() { return id; }
    public void setId(Long id) { this.id = id; }

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public BrandEntity getBrand() { return brand; }
    public void setBrand(BrandEntity brand) { this.brand = brand; }

    public DistributorEntity getDistributor() { return distributor; }
    public void setDistributor(DistributorEntity distributor) { this.distributor = distributor; }

    public FactoryEntity getFactory() { return factory; }
    public void setFactory(FactoryEntity factory) { this.factory = factory; }

    public String getPlushieCategory() { return plushieCategory; }
    public void setPlushieCategory(String plushieCategory) { this.plushieCategory = plushieCategory; }

    /**
     * Converts to a domain record.
     * ⚠️ Because relationships are LAZY, this method MUST run inside a transaction
     * or with an open session to avoid LazyInitializationException.
     * Consider using a DTO or a JOIN FETCH query in the repository instead.
     */
    public Plushie toRecord() {
        PlushieCategory category;
        try {
            category = PlushieCategory.valueOf(plushieCategory);
        } catch (Exception e) {
            throw new IllegalArgumentException("Invalid category for row: " + this.id);
        }
        // Extract brand, distributor and factory names (lazy loaded)
        String brandName = this.brand.getName();
        String distributorName = this.distributor.getName();
        String factoryName = this.factory.getName();
        return new Plushie(id, name, brandName, distributorName, factoryName, category);
    }

    /**
     * Creates a PlushieEntity from a Plushie record.
     * You’ll likely need to look up the actual BrandEntity, DistributorEntity and
     * FactoryEntity from the database before calling this factory. Here we only store
     * the references and assume the entities are already persisted. The id is generated by the DB.
     */
    public static PlushieEntity fromRecord(Plushie plushie, BrandEntity brand, DistributorEntity distributor, FactoryEntity factory) {
        String rawCategory = Optional.ofNullable(plushie.plushieCategory())
                .map(Enum::name)
                .orElse(null);
        return new PlushieEntity(plushie.name(), brand, distributor, factory, rawCategory);
    }
}
